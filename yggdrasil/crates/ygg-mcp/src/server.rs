#[tool_handler]
impl ServerHandler for YggdrasilServer {
    fn get_info(&self) -> ServerInfo {
        ServerInfo::new(
            ServerCapabilities::builder()
                .enable_tools()
                .enable_resources()
                .enable_logging()
                .build(),
        )
        .with_server_info(Implementation::new("yggdrasil", env!("CARGO_PKG_VERSION")))
    }

    async fn list_resources(
        &self,
        _request: Option<PaginatedRequestParams>,
        _context: rmcp::service::RequestContext<rmcp::RoleServer>,
    ) -> Result<ListResourcesResult, rmcp::ErrorData> {
        Ok(ListResourcesResult::with_all_items(vec![
            RawResource::new(RESOURCE_MODELS, "Available Models")
                .with_description("Lists all LLM models available through Odin.")
                .with_mime_type("text/plain")
                .no_annotation(),
            RawResource::new(RESOURCE_MEMORY_STATS, "Memory Statistics")
                .with_description("Engram count and tier statistics from Mimir.")
                .with_mime_type("text/plain")
                .no_annotation(),
            RawResource::new(RESOURCE_SESSION_CONTEXT, "Session Context")
                .with_description("Prefetched active sprint context from Mimir memory.")
                .with_mime_type("text/plain")
                .no_annotation(),
        ]))
    }

    fn read_resource(
        &self,
        request: ReadResourceRequestParams,
        _context: rmcp::service::RequestContext<rmcp::RoleServer>,
    ) -> impl std::future::Future<
        Output = Result<ReadResourceResult, rmcp::ErrorData>,
    > + Send + '_ {
        let client = self.client.clone();
        let config = self.config.clone();
        let context_cache = Arc::clone(&self.context_cache);
        async move {
            match request.uri.as_str() {
                RESOURCE_MODELS => {
                    Ok(crate::resources::read_models_resource(&client, &config).await)
                }
                RESOURCE_MEMORY_STATS => {
                    Ok(crate::resources::read_memory_stats_resource(&client, &config).await)
                }
                RESOURCE_SESSION_CONTEXT => {
                    Ok(crate::resources::read_session_context_resource(&context_cache))
                }
                other => Err(rmcp::ErrorData::invalid_params(
                    format!("Unknown resource URI: {}", other),
                    None,
                )),
            }
        }
    }
}
